SELECT
    Age_Group,
    SUM(Purchase_Amount) AS Total_Revenue,
    ROUND(
        SUM(Purchase_Amount) * 100.0
        / (SELECT SUM(Purchase_Amount) FROM customers), 2
    ) AS Revenue_Pct
FROM customers
GROUP BY Age_Group
